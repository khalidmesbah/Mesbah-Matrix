// Functions
export const getRandomAyah = () =>
  Math.floor(
    Math.random() * (MAXIMUM_NUMBER_OF_AYAHS - MINIMUM_NUMBER_OF_AYAHS + 1) +
      MINIMUM_NUMBER_OF_AYAHS,
  );
export const getRandomSurah = () =>
  Math.floor(
    Math.random() * (MAXIMUM_NUMBER_OF_SURAHS - MINIMUM_NUMBER_OF_SURAHS + 1) +
      MINIMUM_NUMBER_OF_SURAHS,
  );
export const checkIsPrevNumberOfAyahValid = (numberOfAyah: number, prev: number) => {
  return numberOfAyah - prev < MINIMUM_NUMBER_OF_AYAHS ? numberOfAyah : numberOfAyah - prev;
};
export const checkIsNextNumberOfAyahValid = (numberOfAyah: number, next: number) => {
  return numberOfAyah + next > MAXIMUM_NUMBER_OF_AYAHS ? numberOfAyah : numberOfAyah + next;
};
export const getNumberOfAyah = (surah: number, ayah: number): number => {
  let numberOfAyah = 0;

  for (let i = 1; i < surah; i++) {
    numberOfAyah += SURAH_AYAHS[i];
  }
  numberOfAyah += ayah;

  return numberOfAyah;
};
