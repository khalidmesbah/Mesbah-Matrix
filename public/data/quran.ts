const checkIsPrevNumberOfAyahValid = (numberOfAyah: number, prev: number) => {
  return numberOfAyah - prev < MINIMUM_NUMBER_OF_AYAHS ? numberOfAyah : numberOfAyah - prev;
};
const checkIsNextNumberOfAyahValid = (numberOfAyah: number, next: number) => {
  return numberOfAyah + next > MAXIMUM_NUMBER_OF_AYAHS ? numberOfAyah : numberOfAyah + next;
};

const getNumberOfAyah = (surah: number, ayah: number): number => {
  let numberOfAyah = 0;

  for (let i = 1; i < surah; i++) {
    numberOfAyah += SURAH_AYAHS[i];
  }
  numberOfAyah += ayah;

  return numberOfAyah;
};

